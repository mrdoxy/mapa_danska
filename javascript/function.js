$(function () {
    let color = null;

    //státy
    $('path').mouseover(function () {
        color = $(this).css('fill');
        $(this).css('fill', 'red');
    })
    $('path').on('mouseleave', function () {
        $(this).css('fill', color);
    })
    $('[data-toggle="popover"]').popover();

    //města
    $('rect').mouseover(function () {
        color = $(this).css('fill');
        $(this).css('fill', 'white');
    })
    $('rect').on('mouseleave', function () {
        $(this).css('fill', color);
    })
    $('rect').on('click', function () {
        let mesto = mesta.find(item => {
            return item.nazev == $(this).attr('id')
        });

        $('h2').text(mesto.nazev);
        $('p').text(mesto.text);
        $('img').attr('src', mesto.img);
        $('img').attr('style',"width: 300px");
    })

    //památky
    $('ellipse').mouseover(function () {
        color = $(this).css('fill');
        $(this).css('fill', 'black');
    })
    $('ellipse').on('mouseleave', function () {
        $(this).css('fill', color);
    })
    $('ellipse').on('click', function () {
        $('h2').text($(this).find('title').text())
        $(this).css('fill', 'black');

        let pamatka = pamatky.find(item => {
            return item.id == $(this).attr('id')
        });

        $('h2').text(pamatka.nazev);
        $('p').text(pamatka.text);
        $('img').attr('src', pamatka.img);
        $('img').attr('style',"width: 100%;")
    })
})